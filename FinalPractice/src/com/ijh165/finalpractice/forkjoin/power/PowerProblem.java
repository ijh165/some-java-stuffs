package com.ijh165.finalpractice.forkjoin.power;

public class PowerProblem {

    private final long n;
    private final long i;

    public PowerProblem(long n, long i) {
        this.n = n;
        this.i = i;
    }

    public long getN() {
        return n;
    }

    public long getI() {
        return i;
    }
}